public class Pembagian {
    double bilangan1;
    double bilangan2;


    public Pembagian(double bilangan1, double bilangan2) {
        this.bilangan1 = bilangan1;
        this.bilangan2 = bilangan2;
    }

    public double hasilPembagian() {
        return this.bilangan1 / this.bilangan2;
    }
}