public class Perkalian {
    double bilangan1;
    double bilangan2;


    public Perkalian(double bilangan1, double bilangan2) {
        this.bilangan1 = bilangan1;
        this.bilangan2 = bilangan2;
    }

    public double hasilPerkalian() {
        return this.bilangan1 * this.bilangan2;
    }
}
