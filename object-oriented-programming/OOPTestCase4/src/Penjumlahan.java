public class Penjumlahan {
        double bilangan1;
        double bilangan2;


        public Penjumlahan(double bilangan1, double bilangan2) {
            this.bilangan1 = bilangan1;
            this.bilangan2 = bilangan2;
        }

        public double hasilPenjumlahan() {
            return this.bilangan1 + this.bilangan2;
        }
}
