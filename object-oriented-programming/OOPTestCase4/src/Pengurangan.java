public class Pengurangan {
    double bilangan1;
    double bilangan2;


    public Pengurangan(double bilangan1, double bilangan2) {
        this.bilangan1 = bilangan1;
        this.bilangan2 = bilangan2;
    }

    public double hasilPengurangan() {
        return this.bilangan1 - this.bilangan2;
    }
}
